# Hacer un programa que dadas tres notas
# parciales A,B,C y calcule el promedio.

parcialA = int(input("Ingrese la nota del parcial A\n"))
parcialB = int(input("Ingrese la nota del parcial B\n"))
parcialC = int(input("Ingrese la nota del parcial C\n"))

promedio = int((parcialA + parcialB + parcialC) / 3)

print(promedio)
